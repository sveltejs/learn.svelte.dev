<script>
	/** @type {{
	 * 	path: string;
	 * 	loading: boolean;
	 * 	href: string | null;
	 * 	on_refresh?: () => void;
	 * 	on_change?: (value: string) => void;
	 * 	on_toggle_terminal?: () => void;
	 * }}*/
	let { path, loading, href, on_refresh, on_change, on_toggle_terminal } = $props();
</script>

<div class="chrome" class:loading>
	<button
		disabled={loading}
		class="reload icon"
		onclick={() => on_refresh?.()}
		aria-label="reload"
	/>

	<input
		disabled={loading}
		aria-label="URL"
		value={path}
		onchange={(e) => {
			on_change?.(e.currentTarget.value);
		}}
		onkeydown={(e) => {
			if (e.key !== 'Enter') return;

			on_change?.(e.currentTarget.value);
			e.currentTarget.blur();
		}}
	/>

	<a
		{href}
		class="new-tab icon"
		target="_blank"
		aria-label={href ? 'open in new tab' : undefined}
		tabindex="0"
	/>

	<button
		disabled={loading}
		class="terminal icon"
		onclick={() => on_toggle_terminal?.()}
		aria-label="toggle terminal"
	/>
</div>

